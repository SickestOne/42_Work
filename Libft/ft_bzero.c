#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*s_cpy;

	i = 0;
	s_cpy = s;
	while (i < n)
	{
		*s_cpy = 0;
		s_cpy++;
		i++;
	}
}
