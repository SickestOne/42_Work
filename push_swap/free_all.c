#include "push_swap.h"

void	free_all_data(t_ctrl *ab)
{
	t_node	*a;

	a = ab->a;
	while (ab->a != NULL)
	{
		a = ab->a->next;
		free(ab->a);
		ab->a = a;
	}
	free(ab);
}
