#include "ft_printf.h"

int	ft_do_s(char *str)
{
	int	i;

	i = 0;
	if (!str)
	{
		if (ft_do_s("(null)") < 0)
			return (-2147483648);
		return (6);
	}
	while (str[i])
	{
		if (ft_do_c(str[i++]) == -2147483648)
			return (-2147483648);
	}
	return (i);
}
